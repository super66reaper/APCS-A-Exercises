public class Quadradic2 {

    public static void main (String [] args) {
        double x = 0.0;
        double answer = (x * x * 3) - (8 * x) + 4;
        System.out.println("The answer with x at " + x + " is: " + answer);

        x = 2.0;
        answer = (x * x * 3) - (8 * x) + 4;
        System.out.println("The answer with x at " + x + " is: " + answer);

        x = 4.0;
        answer = (x * x * 3) - (8 * x) + 4;
        System.out.println("The answer with x at " + x + " is: " + answer);

    }
}